#' My name and favorite color function.
#'
#' @param name Character value of a name.
#' @param color Character value of a color.
#'
#' @return A sentence stating a person's (my) name and their (my) favorite color.
#' @export
#'
#' @examples
#' my_name("Shayna","blue")
my_name <- function(name, color){
  print(paste("My name is", name,"and my favorite color is",color))
}
